// Helper functions
export const getTeamsList = () => {
  return Object.entries(NFL_TEAMS).map(([abbr, team]) => ({
    abbreviation: abbr,
    ...team,
  }));
};

export const getTeamByAbbreviation = (abbr) => {
  return NFL_TEAMS[abbr] || null;
};

export const getTeamLogo = (abbr) => {
  const team = NFL_TEAMS[abbr];
  return team ? team.logo : 'üèà';
};

